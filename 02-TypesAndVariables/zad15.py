degrees_celcius = float(input("Temperature degrees in celcius: ")) #users input - celcius
degrees_kelvin = degrees_celcius + 273.15 #formula to convert celcius to kelvins
degrees_fahrenheit = degrees_celcius*1.8 + 32 #formula to convert celcius to fahrenheit
print (f"Kelvins {degrees_kelvin}") #prints temperature in kalvins 
print(f"Fahrenheits {degrees_fahrenheit}") #prints temperature in fahrenheits 